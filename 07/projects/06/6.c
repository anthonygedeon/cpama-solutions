#include <stdio.h>

int main(void) {
	printf("value of sizeof(int) is: %zu\n", sizeof(int));
	printf("value of sizeof(short) is: %zu\n", sizeof(short));
	printf("value of sizeof(long) is: %zu\n", sizeof(long));
	printf("value of sizeof(double) is: %zu\n", sizeof(double));
	printf("value of sizeof(long double) is: %zu\n", sizeof(long double));

	return 0;
}
